// 將繁體中文轉換為簡體中文
function convertToSimplified(text) {
    if (typeof text !== 'string') return text;

    let result = text;
    for (const [trad, simp] of Object.entries(traditionalToSimplified)) {
        result = result.split(trad).join(simp);
    }
    return result;
}

// 遞歸處理 JSON 對象，只轉換 zh-Hans 字段
function processObject(obj) {
    if (Array.isArray(obj)) {
        return obj.map(item => processObject(item));
    }

    if (obj !== null && typeof obj === 'object') {
        const result = {};
        for (const [key, value] of Object.entries(obj)) {
            if (key === 'zh-Hans' && typeof value === 'string') {
                // 只轉換 zh-Hans 字段
                result[key] = convertToSimplified(value);
            } else {
                result[key] = processObject(value);
            }
        }
        return result;
    }

    return obj;
}

// 處理文件
function processFile(filePath) {
    console.log(`處理文件: ${filePath}`);

    try {
        const content = fs.readFileSync(filePath, 'utf8');
        const data = JSON.parse(content);
        const converted = processObject(data);

        // 備份原文件
        const backupPath = filePath + '.backup-hans';
        fs.writeFileSync(backupPath, content);
        console.log(`  已備份到: ${backupPath}`);

        // 寫入轉換後的內容
        fs.writeFileSync(filePath, JSON.stringify(converted, null, 2), 'utf8');
        console.log(`  轉換完成!`);

        return true;
    } catch (error) {
        console.error(`  錯誤: ${error.message}`);
        return false;
    }
}

// 主程序
const filesToProcess = [
    'questions-all.json',
    'questions-basics.json',
    'questions-deepdive.json',
    'questions-final.json',
    'questions-signs.json'
];

console.log('開始繁體轉簡體轉換...\n');

for (const fileName of filesToProcess) {
    const filePath = path.join(__dirname, fileName);
    if (fs.existsSync(filePath)) {
        processFile(filePath);
    } else {
        console.log(`文件不存在: ${fileName}`);
    }
    console.log('');
}

