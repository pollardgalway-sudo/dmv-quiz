// 简体中文转繁体中文
function convertToTraditional(text) {
    if (typeof text !== 'string') return text;
    
    // Step 1: 词语级别替换（处理一对多映射）
    let result = text;
    for (const [simplified, traditional] of wordReplacements) {
        result = result.split(simplified).join(traditional);
    }
    
    // Step 2: 逐字替换
    let finalResult = '';
    for (let i = 0; i < result.length; i++) {
        const char = result[i];
        finalResult += simplifiedToTraditional[char] || char;
    }
    return finalResult;
}

// 递归处理 JSON 对象，将 zh-Hant 字段用 zh-Hans 内容转换为繁体
function processObject(obj) {
    if (Array.isArray(obj)) {
        return obj.map(item => processObject(item));
    }

    if (obj !== null && typeof obj === 'object') {
        const result = {};
        for (const [key, value] of Object.entries(obj)) {
            if (key === 'zh-Hant' && typeof value === 'string') {
                // 找到对应的 zh-Hans 字段（在同级对象中）
                const hansValue = obj['zh-Hans'];
                if (hansValue) {
                    // 使用 zh-Hans 的内容转换为繁体
                    result[key] = convertToTraditional(hansValue);
                } else {
                    // 如果没有 zh-Hans，直接转换当前 zh-Hant 的内容
                    result[key] = convertToTraditional(value);
                }
            } else if (key === 'analysis_zh-Hant' && typeof value === 'string') {
                const hansAnalysis = obj['analysis_zh-Hans'];
                if (hansAnalysis) {
                    result[key] = convertToTraditional(hansAnalysis);
                } else {
                    result[key] = convertToTraditional(value);
                }
            } else {
                result[key] = processObject(value);
            }
        }
        return result;
    }

    return obj;
}

// 处理文件
function processFile(filePath) {
    console.log(`处理文件: ${filePath}`);

    try {
        const content = fs.readFileSync(filePath, 'utf8');
        const data = JSON.parse(content);
        const converted = processObject(data);

        // 写入转换后的内容
        fs.writeFileSync(filePath, JSON.stringify(converted, null, 2), 'utf8');
        
        // 统计转换数量
        const originalStr = JSON.stringify(data);
        const convertedStr = JSON.stringify(converted);
        const changedChars = [...convertedStr].filter((char, i) => char !== originalStr[i]).length;
        console.log(`  ✅ 转换完成! 约 ${changedChars} 个字符被转换`);

        return true;
    } catch (error) {
        console.error(`  ❌ 错误: ${error.message}`);
        return false;
    }
}

// 验证转换结果
function verifyConversion(filePath) {
    try {
        const content = fs.readFileSync(filePath, 'utf8');
        const data = JSON.parse(content);
        
        let totalHant = 0;
        let stillSimplified = 0;
        const simplifiedChars = new Set(Object.keys(simplifiedToTraditional).filter(k => k !== simplifiedToTraditional[k]));
        
        function checkObject(obj) {
            if (Array.isArray(obj)) {
                obj.forEach(checkObject);
                return;
            }
            if (obj !== null && typeof obj === 'object') {
                for (const [key, value] of Object.entries(obj)) {
                    if ((key === 'zh-Hant' || key === 'analysis_zh-Hant') && typeof value === 'string') {
                        totalHant++;
                        // Check if any simplified-only characters remain
                        for (const char of value) {
                            if (simplifiedChars.has(char)) {
                                stillSimplified++;
                                break;
                            }
                        }
                    } else {
                        checkObject(value);
                    }
                }
            }
        }
        
        checkObject(data);
        console.log(`  验证: ${totalHant} 个繁体字段, ${stillSimplified} 个可能仍含简体`);
    } catch (error) {
        console.error(`  验证错误: ${error.message}`);
    }
}

